number = 0
print("number 변수의 초기값은", number, "입니다.")

print("number 변수에 10을 더하겠습니다.")
number += 10 # (주석) number = number + 10
print("number에 10을 더한 후 :", number)

print("number변수에 3을 곱하겠습니다.")
number *= 3 # (주석) number = number * 10
print("number에 3을 곱한 후 :", number)

print("number변수에 4를 빼겠습니다.")
number -= 4 # (주석) number = number - 10
print("number에 4를 뺀 후 :",number)
